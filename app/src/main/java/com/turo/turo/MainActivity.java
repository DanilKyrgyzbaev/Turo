package com.turo.turo;

import androidx.annotation.NonNull;
import androidx.appcompat.app.AppCompatActivity;
import androidx.fragment.app.Fragment;

import android.os.Bundle;
import android.view.MenuItem;

import com.google.android.material.bottomnavigation.BottomNavigationView;
import com.turo.turo.fragments.HomeFragment;
import com.turo.turo.fragments.MenuFragment;
import com.turo.turo.fragments.PersonFragment;

public class MainActivity extends AppCompatActivity {
    private Fragment fragment;
    private HomeFragment homeFragment;
    private MenuFragment menuFragment;
    private PersonFragment personFragment;
    BottomNavigationView bottomNavigationView;

    private BottomNavigationView.OnNavigationItemSelectedListener onNavigationItemSelectedListener = new BottomNavigationView.OnNavigationItemSelectedListener() {
        @Override
        public boolean onNavigationItemSelected(@NonNull MenuItem item) {

            switch (item.getItemId()){
                case R.id.home:
                    fragment = homeFragment;
                break;
                case R.id.menu:
                    fragment = menuFragment;
                    break;
                case R.id.person:
                    fragment = personFragment;
                    break;
            }

            getSupportFragmentManager().beginTransaction()
                    .replace(R.id.bottom_frame,fragment).commit();

            return true;
        }
    };
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        BottomNavigationView navView = findViewById(R.id.bottom_navigation_view);

        fragment = null;
        homeFragment = HomeFragment.newInstance();
        menuFragment = MenuFragment.newInstance();
        personFragment = PersonFragment.newInstance();
        navView.setOnNavigationItemSelectedListener(onNavigationItemSelectedListener);
        navView.setSelectedItemId(R.id.menu);


    }
}









































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































